						<td class="bodytext"><p><img src="images/mym/MYM logo no co-branding.gif" alt="MYM Logo" width="140" height="55" hspace="12" vspace="12" align="right" />You could be gazing at the animal of your dreams, but you just can&rsquo;t  tell what she&rsquo;s thinking behind that furry face. What if you knew a  little something about her personality and habits before you moved in  together? The Meet Your Match&trade; program wouldn&rsquo;t let you go home  without knowing who&rsquo;s in that carrier or on that leash. MYM is the only  method in existence today that evaluates an animal&rsquo;s behavior and  interests and matches them to an adopter&rsquo;s preferences so that you take  home a pet you can really click with. Meet Your Match&trade; is a program of ASPCA. </p>
							<p><strong>CANINE-ALITY</strong></p>
							<p>The Meet Your Match&trade; Canine-ality&trade;  assessment begins  with an initial MYM 
								SAFER&trade; (Safety Assessment for Evaluating Rehoming)  behavior screening to make 
								sure each pooch is a good candidate for  adoption. Dogs are then tested on their 
								friendliness, playfulness,  energy level, motivation and drive, and placed into one of 
								nine  color-coded Canine-alities. Some pooches are laid-back &ldquo;Couch  Potatoes,
								&rdquo; others are curious &ldquo;Busy Bees,&rdquo; and then there are the  action hero 
								&ldquo;Go-Getter&rdquo; types.</p>

							<p>As an adopter, you also get a color based on your preferences and  lifestyle. On your 
								visit to the adoption facility, you&rsquo;re given a  purple, orange or green guest 
								pass to match the pets who might suit you  best.</p>
							<ul>
								<li>Green adopters are most successful with pooches who like to be physically and 
									mentally engaged. </li>
								<li>Orange adopters are a good fit with middle-of-the-road dogs who are responsive 
									and enjoy regular activity and interaction. </li>
								<li>Purple adopters are comfortable with canines who have a laidback attitude and 
									prefer an easygoing lifestyle.</li>
							</ul>

							<p>Now you&rsquo;re off&mdash;with a color-coded guest pass in hand&mdash;to find your new  
								companion. As you pass each furry face, you&rsquo;ll notice the dogs have  color cards 
								on their cages. You don&rsquo;t have to choose a pooch whose  color matches yours&mdash;
								love at first sight does happen. But knowing the  color lets you at least prepare for 
								the way your four-legged love will  respond when he gets home.</p>
							<p align="center"><a href="dogsurvey.php"><img src="images/mym/dog_survey.gif" 
								width="140" height="78" border="0" alt="Take the Dog Survey!"></a></p>
							<p><strong>FELINE-ALITY</strong></p>
							<p>Each adoptable cat is assessed based on level of interest in play,  exploring, &ldquo;
								talking,&rdquo; and being the center of attention so that he or  she can be placed into 
								one of the nine color-coded &ldquo;feline-ality&rdquo; types  that make up the Meet 
								Your Match program. Green cats are savvy and  adventurous, orange cats make the perfect 
								companions and purple kitties  are quiet, seek affection and stay out of trouble. The 
								color is  indicated on the cat&rsquo;s kennel card, along with a short description, so  
								you&rsquo;ll know just who you&rsquo;re dealing with in your quest for your dream  
								kitty.</p>

							<p>Let&rsquo;s say you are looking to find the cat of your dreams. When you  go to a 
								shelter using the Feline-ality assessment, you&rsquo;ll be asked to  fill out a 
								one-page Cat Adopter Survey&mdash;19 questions about your  lifestyle and how you 
								envision your cat fitting in to this lifestyle.  Sound kinda fun? It is&mdash;think 
								magazine quiz, not college application. </p>
							<p>Your results will tell the shelter what your &ldquo;color&rdquo; is&mdash;green, orange 
								or purple. </p>
							<p>In a nutshell: </p>

							<ul>
								<li>Green adopters are most successful with cats who quickly adapt to new situations. </li>
								<li>Purple adopters are perfect for kitties who need time and encouragement to adjust to their new surroundings. </li>
								<li>Orange adopters are a good fit with the quintessential companion kitty. </li>
							</ul>
							<p>P.S. In case you haven&rsquo;t figured it out yet, the kitties in the  shelter know 
								their true colors, too&mdash;so purple people meet purple  felines, and then decide if 
								they&rsquo;re ready to paint the town&hellip;well,  purple! </p>

							<p>But don&rsquo;t worry if you&rsquo;d rather mix-and-match&mdash;adopters are not 
								required to choose a cat with a particular feline-ality, and the  assessment can prepare 
								you for your new love&rsquo;s response to her new home  and help her adjust. </p>
