	#* createThumbs(string pathToImage, string pathToThumbs, int thumbWidth)
	#* creates a thumbnail of an image and saves it in a different folder
	function createThumbs($pathToImage, $pathToThumbs, $thumbWidth) {
		// change permissions for the "images/thumbnails/" folder 
		chmod($pathToThumbs, 0666);
		
		// parse path for the extension
		$info = pathinfo($pathToImage);
		
		// continue only if this is a JPEG image
		if(strtolower($info['extension']) == 'jpg') {
			// load image and get image size
			$img = imagecreatefromjpeg("{$pathToImage}");
			$width = imagesx($img);
			$height = imagesy($img);

			// calculate thumbnail size
			$new_width = $thumbWidth;
			$new_height = floor($height * ($thumbWidth / $width));

			// create a new temporary image
			$tmp_img = imagecreatetruecolor($new_width, $new_height);

			// copy and resize old image into new image 
			imagecopyresized($tmp_img, $img, 0, 0, 0, 0, $new_width, $new_height, $width, $height);

			// save thumbnail into a file
			$thumbFileName = $pathToThumbs . $info['basename'];
			//echo "<p>$thumbFileName</p>.";
			imagejpeg($tmp_img, $thumbFileName);
			return $thumbFileName;
		}
	}
	
